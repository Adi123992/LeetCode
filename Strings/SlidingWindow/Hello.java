package Strings.SlidingWindow;

public class Hello {
    public static void main(String[] args) {
        String s = "Aditya";
        int i = 50;
        System.out.println(i);
        for (i = 0; i < s.length(); i++) {
            if (i % 2 == 0) System.out.println("TOing");
        }
        for (i = 0; i < s.length() / 2; i++) {
            if (i % 2 == 0) System.out.println("Inupinu");
        }
    }
}
